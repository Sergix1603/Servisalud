private void updateCombo(){
        Statement st;
        Conexion con = new Conexion();
        Connection conexion = con.getConexion();
        String sql  = "select * from sucursales ";
        try {
                st=conexion.prepareStatement(sql);
                ResultSet rs = st.executeQuery(sql);
                while(rs.next()){
                    Jcombo_cod_sede.addItem(rs.getString("cod_sucursal"));
                }
            } catch (Exception e) {
            }
    }
private void updateCombo2(){
        Statement st;
        Conexion con = new Conexion();
        Connection conexion = con.getConexion();
        String sql  = "select * from empleados ";
        try {
                st=conexion.prepareStatement(sql);
                ResultSet rs = st.executeQuery(sql);
                while(rs.next()){
                    String apellido = rs.getString("emple_ape");
                    String otraColumna = rs.getString("Especialidad");
                    String valorCombo = apellido + " - " + otraColumna;
                    Jcombo_ape_emple.addItem(valorCombo);
                }
            } catch (Exception e) {
            }
    }
